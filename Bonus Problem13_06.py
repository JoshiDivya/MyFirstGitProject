A=[3,1,2,4,3]
dif=sum(A)
for i in range(len(A)-1):
    i+=1
    a=sum(A[:i])
    b=sum(A[i:])
    diff= abs(a-b)
    if diff<dif:
        dif=diff
print(dif)


